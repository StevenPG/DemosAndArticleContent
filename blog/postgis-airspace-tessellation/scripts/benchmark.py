"""
Answer the practicality question with numbers: how long do tessellation and
3D conflict detection take as the cell size shrinks?

For each resolution this measures:
  - tessellation (grid + terrain sampling + extrusion + solids, into the
    n-d GiST indexed table)
  - ST_3DIntersects pass (indexed &&& prefilter; over-reports touching faces)
  - prism-exact pass (2D area x z-interval; correct volumes, no CSG)
  - CSG pass sampled on CSG_SAMPLE pairs (ST_Volume(ST_3DIntersection)),
    extrapolated to the full candidate set to show what it *would* cost

Usage: python scripts/benchmark.py [sizes...]        (default: 100 50 25)
Writes benchmark_results.md next to the README.
"""
import pathlib
import sys
import time

from db import connect

OUT = pathlib.Path(__file__).resolve().parent.parent / "benchmark_results.md"
CSG_SAMPLE = 200


def timed(cur, sql, params=()):
    t0 = time.perf_counter()
    cur.execute(sql, params)
    rows = cur.fetchall() if cur.description else None
    return time.perf_counter() - t0, rows


def main() -> None:
    sizes = [float(s) for s in sys.argv[1:]] or [100.0, 50.0, 25.0]
    results = []

    with connect() as conn, conn.cursor() as cur:
        for size in sizes:
            print(f"=== cell size {size:g} m ===")
            t_build, _ = timed(cur, "CALL build_airspace_cells(%s)", (size,))
            conn.commit()
            cur.execute("SELECT count(*) FROM airspace_cells")
            n_cells = cur.fetchone()[0]
            print(f"  build: {t_build:.2f}s ({n_cells} cells)")

            t_hits, hits = timed(cur, "SELECT count(*) FROM find_conflicts_intersects()")
            n_candidates = hits[0][0]
            print(f"  ST_3DIntersects: {t_hits:.2f}s "
                  f"({n_candidates} intersecting pairs, touching included)")

            t_prism, prism = timed(cur, """
                SELECT count(*), coalesce(sum(volume_m3), 0)
                FROM find_conflicts_prism()
            """)
            n_true, total_vol = prism[0]
            print(f"  prism-exact: {t_prism:.2f}s "
                  f"({n_true} true conflicts, {total_vol:,.0f} m³ shared)")

            n_sample = min(CSG_SAMPLE, n_candidates)
            t_csg, _ = timed(cur, "SELECT count(*) FROM find_conflicts_csg(%s)", (n_sample,))
            per_pair_ms = t_csg / n_sample * 1000 if n_sample else 0.0
            t_csg_full_est = per_pair_ms / 1000 * n_candidates
            print(f"  CSG (sampled {n_sample}): {per_pair_ms:.1f} ms/pair "
                  f"-> est. {t_csg_full_est:,.0f}s for all candidates")

            results.append((size, n_cells, t_build, t_hits, n_candidates,
                            t_prism, n_true, total_vol, per_pair_ms, t_csg_full_est))

        cur.execute("SELECT * FROM conflict_summary")
        summary = cur.fetchall()

    lines = [
        "# Benchmark results",
        "",
        "Machine-generated by `scripts/benchmark.py`. Times are wall-clock seconds",
        "against local PostGIS. `3D-intersects pairs` includes face-touching cells",
        "(the stacked ALPHA/BRAVO boundary); `true conflicts` excludes them.",
        "The CSG column is measured on a sample and extrapolated to the full",
        "candidate set — it is the cost you'd pay for arbitrary (non-prism) solids.",
        "",
        "| cell size | cells | tessellation (s) | 3D-intersects (s) | 3D-intersects pairs "
        "| prism-exact (s) | true conflicts | shared volume (m³) | CSG ms/pair | CSG full est. (s) |",
        "|---:|---:|---:|---:|---:|---:|---:|---:|---:|---:|",
    ]
    for (size, n_cells, t_build, t_hits, n_candidates,
         t_prism, n_true, total_vol, per_pair_ms, t_csg_full_est) in results:
        lines.append(
            f"| {size:g} m | {n_cells} | {t_build:.2f} | {t_hits:.2f} | {n_candidates} "
            f"| {t_prism:.2f} | {n_true} | {total_vol:,.0f} "
            f"| {per_pair_ms:.1f} | {t_csg_full_est:,.0f} |"
        )

    lines += ["", "## Conflicting airspace pairs (last run)", "",
              "| pair | conflicting cell pairs | shared volume (m³) |", "|---|---:|---:|"]
    for a, b, pairs, vol in summary:
        lines.append(f"| {a} × {b} | {pairs} | {vol:,} |")

    OUT.write_text("\n".join(lines) + "\n")
    print(f"\nWrote {OUT}")


if __name__ == "__main__":
    main()
